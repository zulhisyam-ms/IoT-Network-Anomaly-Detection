    for i in range(len(device_types)):
        for j in range(i + 1, len(device_types)):
            dev1, dev2 = device_types[i], device_types[j]
            idx1 = device_indices[dev1]
            idx2 = device_indices[dev2]

            max_len = max(len(idx1), len(idx2))
            idx1 *= (max_len // len(idx1)) + 1
            idx2 *= (max_len // len(idx2)) + 1

            mix_pairs.extend(list(zip(idx1[:max_len], idx2[:max_len])))

