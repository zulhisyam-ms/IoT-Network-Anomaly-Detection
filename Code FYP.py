def calculate_feature_variance(df):
    """Calculates the variance of the features (Source Port, Destination Port), handling non-numeric values."""
    # Ensure that the 'Source Port' and 'Destination Port' are numeric
    df['Source Port'] = pd.to_numeric(df['Source Port'], errors='coerce')
    df['Destination Port'] = pd.to_numeric(df['Destination Port'], errors='coerce')

    # Drop rows with NaN values in the numeric columns
    df_clean = df.dropna(subset=['Source Port', 'Destination Port'])

    # Calculate and return the variance
    return np.var(df_clean[['Source Port', 'Destination Port']], axis=0).mean()

